        王奕盛是台灣劇場界最活躍的影像設計師之一。2001年首度與雲門合作，為《行草》設計投影的書法動畫。2006年起陸續為《風．影》、《聽河》、《屋漏痕》、《稻禾》、《白水》、《微塵》設計影像。2013年《屋漏痕》世界劇場設計展互動與新媒體設計類銅牌獎；隔年，《稻禾》獲得英國劇場界專為劇場燈光、投影與多媒體設計設置的「照明騎士」影像設計大獎。2017年再以雲門２《十三聲》影像設計獲得世界劇場設計展投影與多媒體設計類銀牌獎。
