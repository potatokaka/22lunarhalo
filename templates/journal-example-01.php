            <p>「這22度暈指的到底是什麼，有誰可以解釋清楚嗎？」<br>
            此話一出，會議室中的所有人無不拾起手機，低頭搜尋。</p>

            <p>22度暈是在太陽或月亮周圍形成的22°的暈，由大氣層中懸浮的大量六邊形冰晶反射陽光形成。— (引用自維基百科)</p>

            <p>有人大聲讀出維基百科中的描述，一行人仍迷迷糊糊的坐在椅子上，遙想宇宙間究竟發生什麼事，當世人抬頭一看，竟讓那原來光滑溜溜的月亮，長出了一圈毛邊。此時，鄭宗龍起身、大手一揮，在白板上畫出了太陽光與月球、月球與地球上的眼睛，這三層關係，在這微妙的22度角時，迸出美幻的月暈。</p>

            <p>「冷空氣形成了許許多多的冰晶，當光線照射到這些冰晶，折射22度之後，將會形成這一圈光圈。」</p>

            <p>圖文並置，眾人這才恍然大悟。其實，月暈和日暈都是光線透過高空時，受到冷晶折射作用，使原來七色複合光被分散，而成為光環或光弧，圍繞在月亮或太陽的周圍。而日暈相對比較常見，因為要在夜晚看見月暈，除了氣候條件相符外，也必須是滿月之時，相對就大大降低了形成「毛月亮」的機會。</p>

            <p>原來這一圈光暈是從無數個巧合碰撞、凝結而成，此種偶遇更顯珍貴，每每發生使人驚豔，卻也不禁讓人有惴惴不安之感。</p>

            <p>有人說，看見「毛月亮」是一種不好的預兆，如有惡運將至、惡鬼降臨。當眼前之事偏離日常經驗，總讓人印象深刻或感不安，那般「未知」也成為某種警示或暗號，將這種「不安」轉移成神話、故事或是民間傳說，亦是人類處理情緒的一種方式。而這長了毛的奇幻月色，則給了編舞家鄭宗龍在創作上無限異想。</p>

            <p>關於毛月亮，天文學家已經把理說盡了，那麼還有其他的遐思，不如就交給編舞家吧！</p>

