            <p>酒吧的熱度沸騰到了極點，然而夜已深，老闆關店喊停之際，一位「詩人」出現在這群醉醺醺的大漢之間，抓了幾個醉漢模擬日、月與地球運行的關係，在這間破舊的小酒吧裡，呈現了宇宙。</p>

            <p>這是匈牙利導演貝拉塔爾的電影《Werckmeister Harmonies》（2000）片段，詩人透過最簡單的日月行星運行，展現世間萬物「永恆」的法則。老闆最終還是把所有人趕出去，然詩人對老闆留下一句「還沒結束」，透露了這個世界的某種永恆不滅。</p>

            <p>龔卓軍老師在課堂中提出「永恆」的想法，如果人會生老病死，揭露永恆的詩人也有一天會離開人世，那麼不滅的究竟是什麼？就像影片中詩人解釋一種天文現象: 日蝕。日蝕的出現會造成人們的惶恐，因為它打破了常規，展現了改變與無法預測。然而在日蝕之後，日月行星仍然長存，接著還會有再下一次日蝕，再次打破穩定…如此周而復始。</p>

            <p>於是，我們跳開一步思考，那些月有陰晴圓缺、人有旦夕禍福，何嘗不是一種「恆定」？有光明同時有黑暗、善惡的出沒、幸與不幸的循環，這些兩極相互抵消，呈現了人類本質上的一無所有。而人們所能做的是：在一無所有中維持純粹前進的態度，展現一種精神的穩定性。如此長久的走下去，似乎是一種接近永恆的狀態。</p>

            <p>舞者們紛紛點頭，或有共鳴、同感。在舞台上承受眾人眼光的他們，總有狀態好壞的更迭，沒有永恆的跳躍，只有沉重落地與用力騰空的交替轉化。在起伏的動態中尋找精神上的穩定，面對月亮的陰暗面，在時間長軸上，試著摸索一種永恆的寧靜狀態。</p>

