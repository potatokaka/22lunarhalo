            <p>「你覺得你是自由的嗎？」</p>

            <p>舞者們總是在舞台上盡情地伸展肢體，好似擁有絕對的自由。但當龔卓軍老師此話一落，眾人卻陷入沉思。</p>

            <p>在創作中，舞者們不僅要動身體，編舞家鄭宗龍更希望舞者可以有感受，帶著問題與無止境的思考，在舞作中探索內心自我。因此邀請龔卓軍老師替大家上課，透過影像、書籍，以開放的討論方式，帶領眾人叩問深埋在心中的各種疑問。</p>

            <p>自由這個問題可能太抽象了，龔卓軍老師換了一個題目：「如果你有小孩，你覺得你的小孩需要上學嗎？那可以是一種選擇嗎？」問題一下子剛好拋向已經有小孩或剛剛脫離學生身分的舞者們，只見大家埋頭苦思，猶豫展露在不斷變化的神情中。下一個問題更為艱難：「思考一下，你成為今天的你，都是你自己自由地選擇來的嗎？」</p>

            <p>有些舞者果斷的回答是，之所以成為舞者，在這裡聽課，都是自己選擇來的。選擇唸舞蹈班、選擇相信編舞家、選擇以舞蹈作為職業。然而有些人對於「選擇與自由」的關係產生質疑。有人提出，這些選擇好像都是「有限度」的：一個人的出生、父母、環境，總有無法選擇的條件，而種種條件的集合，綜合成人的處境。</p>

            <p>想到這裡，課堂的氣氛突然沉靜了下來，那些錯落在人生之中的各道選項都浮現在眼前，生而為人到底有多自由？此刻，沒有人有正確答案。</p>

            <p>帶著這些問題，編舞家與舞者們回到創作排練場上，或許在每一次的動作選擇與呼吸裡，他們都能順著這些被喚醒的提問，每一次都更接近自己心中的答案。</p>

