            <p>延伸資料:</p>

            <p>龔卓軍 簡介<br>
            1966年，出生於台灣嘉義，2006年發表《身體部署：梅洛龐蒂與現象學之後》一書，獲中央研究院年輕學者研究著作獎。現為國立台南藝術大學藝術創作理論研究所副教授兼任所長。</p>

            <p>2009年起，擔任《藝術觀點》(ACT)季刊主編。近年來，龔卓軍開始進行當代藝術界的策展工作，曾於2013年於台北誠品畫廊策畫過「Are We Working Too Much？」展覽；2014年6月於台南策畫「絕對不純粹」東亞論壇：報民/AABB—台南/東京交流計畫、11月於台北鳳甲美術館「鬼魂的迴返」台灣國際錄像藝術展。</p>

            <p>2017年，龔卓軍擔任總策展人的「近未來的交陪：2017蕭壠國際當代藝術節」，引起高度討論，活用文化園區空間與在地連結，進行扎實的田野研究，開展傳統技藝、儀式互動到當代藝術的對話。</p>

